declare(strict_types=1);

namespace Nietzscheson\Admovil;

trait NotesTrait
{

    protected $notes;

    /**
     * @return string
     */
    public function getNotes(): string
    {
        return $this->notes;
    }

    /**
     * @param string $notes
     */
    public function setNotes(string $notes): void
    {
        $this->notes = $notes;
    }
}