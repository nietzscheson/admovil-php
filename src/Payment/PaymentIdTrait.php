declare(strict_types=1);

namespace Nietzscheson\Admovil\Payment;

trait PaymentIdTrait
{

    /**
     * @var string
     */
    private $id;

    /**
     * @return string
     */
    public function getId(): string
    {
        return $this->id;
    }

    /**
     * @param string $id
     */
    public function setId(string $id): void
    {
        $this->id = $id;
    }
}